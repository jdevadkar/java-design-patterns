package com.design.adapter;

/**
 * The Interface Employee.
 */
public interface Employee {

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId();

	/**
	 * Gets the first name.
	 *
	 * @return the first name
	 */
	public String getFirstName();

	/**
	 * Gets the last name.
	 *
	 * @return the last name
	 */
	public String getLastName();

	/**
	 * Gets the email.
	 *
	 * @return the email
	 */
	public String getEmail();
}
